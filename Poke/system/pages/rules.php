<div class='title'>Reglas</div>
<div class='content'>

PokeMasterX es un MMORPG desarrollado junto con jugadores, donde todos los días los jugadores nuevos y viejos enfrentan desafíos nunca antes vistos en el mundo Pokémon.
Para que este juego siga siendo divertido y justo para todos, señalamos algunas políticas en nuestra comunidad, que restringen las actitudes destructivas en el juego o en el sitio web oficial, penalizando, prohibiendo o eliminando personajes y cuentas. Por lo tanto, consideramos que las siguientes acciones están prohibidas:
<br/><br/>
<b>1. Nombres</b><br/>
<b>a)</b> Nombres con insultos, racistas, sexuales, relacionados con drogas u objetables.<br/>
<b>b)</b> Nombres que contienen fragmentos de frases, palabras mal formateadas o combinaciones de letras sin sentido.<br/>
<b>c)</b> Nombres que expresan opiniones políticas o religiosas, que contienen publicidad ilegal o que en general no encajan en el tema de PokeMasterX.<br/>
<b>d)</b> Nombres que implican o incitan a la violación de las Reglas de PokeMasterX.<br/>
<b>e)</b> Nombres que citan a los miembros del equipo o intentan hacerse pasar por ellos mismos.<br/>
<br/><br/>
<b>2. Declaraciones y mensajes</b><br/>
<b>a)</b> Insultos, racismo, sexual, relacionado con drogas, vergonzoso u objetable, especialmente en el canal de ayuda.<br/>
<b>b)</b> No está permitido enviar los mismos mensajes (spam) en el chat público, como el canal de ayuda.<br/>
<b>c) </b> No está permitido anunciar otros servidores, sitios web, otros juegos o cualquier cosa que no esté relacionada con PokeMasterX. <br/>
<b>d) </b> Declaraciones que citan o alientan una violación de las reglas del servidor. <br/>
<b>e) </b> Uso indebido de canales públicos (Game-chat para chatear, Comercio para negociar y Ayuda para preguntas). <br/>
<br/><br/>
<b> 3. Hacer trampa y abuso de Bugs </b> <br/>
<b>a) </b> Explote cualquier error en el juego para su propio beneficio o el de otros. Si encuentra algún error, debe informarlo de inmediato. <br/>
<b>b) </b> Abusar intencionalmente de las debilidades del juego. <br/>
<b>c) </b> Intentar o robar datos de una cuenta que pertenece a otro jugador. <br/>
<b>d) </b> No está permitido hacer ningún tipo de barrera (trampa) para no permitir el paso de aquellos que quieren cazar dentro de cualquier ciudad o salir / entrar de las áreas de caza. <br/>
<b>e) </b> No está permitido jugar demasiados elementos en el mismo SQM con la intención de robar a alguien o incluso depurar al cliente del otro jugador. <br/>
<b>f) </b> Use o aproveche los errores para matar, nivelar, robar o cualquier eventualidad en el servidor. <br/>
<br/> <br/>
<b> 4. Equipo y colaboradores </b> <br/>
<b> a) </b> Amenazar u ofender a un miembro del equipo o colaborador en respuesta a sus acciones como tales en su rol. <br/>
<b> b) </b> Pretendiendo ser un maestro de juego o tratando de influir en sus decisiones. <br/>
<b> c) </b> Proporcionar intencionalmente información errónea o engañosa a un maestro de juego durante sus investigaciones, o hacer informes falsos sobre presuntas infracciones de las Reglas. <br/>
<b> d) </b> No está permitido insultar a ningún miembro de nuestro equipo o colaboradores, tanto en chats públicos como privados. <br/>
<br/> <br/>
<b> 5. Servidor </b> <br/>
<b> a) </b> Todo lo relacionado con el juego, los miembros del equipo no interfieren. <br/>
<b> b) </b> Las cuentas son propiedad de PokeMasterX, pero los jugadores son responsables de ellas, todas las acciones que violen las reglas o el código penal sufrirán las sanciones y sanciones apropiadas. <br/>
<b> c) </b> El intercambio de artículos en otros servidores está prohibido. <br/>
<b> d) </b> Citar el nombre de cualquier otro servidor puede resultar en una prohibición. <br/>
<b> e) </b> Cualquier actitud que no se menciona en las reglas anteriores, pero que es analizada y considerada perjudicial para el juego por cualquier miembro del equipo de PokeMasterX, puede dar lugar a las medidas apropiadas, incluso irreversibles. <br/>
<b> f) </b> PokeMasterX se reserva el derecho de eliminar cuentas y personajes que estén inactivos o que no accedan al juego por un período igual o mayor a 100 días. <br/>
<b> g) </b> PokeMasterX no garantiza la funcionalidad completa de los sistemas y su integración. Pueden producirse pérdidas por parte del jugador, siempre que note inestabilidad o reciba un mensaje de alerta, mantenga sus objetos y personajes en lugares seguros. <br/> <br/>
<br/>
